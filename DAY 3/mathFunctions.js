// Math.ceil() --> round up to the nearest intyeger
// Math.floor() --> rounds down to the nearest integer
// Math.round() --> rounds to the nearest integer
// Math.sqrt() --> square root of a number
// Math.pow() --> power of a number 
// Math.abs() --> absolute value of a number
// Math.min() --> returns the smallest number
// Math.max() --> returns the maximum number
// Math.random() --> returns a random number between o and 1
let x= 5.3;

document.writeln("x= "+x+"<br>");
document.writeln("ceil():- "+Math.ceil(x) + "<br>");
document.writeln("floor():- "+Math.floor(x) + "<br>");
document.writeln("round():- "+Math.round(x) + "<br>");
document.writeln("sqrt():- "+Math.sqrt(x) + "<br>");
document.writeln("pow():- "+Math.pow(x,2) + "<br>");
document.writeln("abs():- "+Math.abs(x) + "<br>");
document.writeln("min():- "+Math.min(1,3,2,4,5) + "<br>");
document.writeln("max():- "+Math.max(1,2,3,4,5) + "<br>");
document.writeln("random():- "+Math.random() + "<br>");
document.writeln("random():- "+Math.round(Math.random()*9999) + "<br>");
document.writeln("cbrt():- "+Math.cbrt(x)+"<br>");
